//Print the DIGIT and ALPHABET
#include<stdio.h>
int main()
{
    int Loop;
    for(Loop=48;Loop<=57;Loop++)        // 48 is 0 ... 57 is 9 as DIGIT
    printf("%c ",Loop);
    printf("\n");
    for(Loop=65;Loop<=90;Loop++)        // 65 is a ... 90 is z as UPPERCASE 
    printf("%c ",Loop);
    printf("\n");
    for(Loop=97;Loop<=122;Loop++)       // 97 is A ... 122 is Z as LOWERCASE
    printf("%c ",Loop);
    printf("\nPUSHPARAJ MANICKAM!!!\n");
    return 0;
}
